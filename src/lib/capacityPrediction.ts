// ---------------------------------------------------------------------------
// Math helpers
// ---------------------------------------------------------------------------

function mean(values: number[]): number {
  if (!values.length) return 0
  return values.reduce((s, v) => s + v, 0) / values.length
}

function stdDev(values: number[], avg?: number): number {
  if (values.length < 2) return 0
  const m = avg ?? mean(values)
  const variance = values.reduce((s, v) => s + (v - m) ** 2, 0) / (values.length - 1)
  return Math.sqrt(variance)
}

function median(sorted: number[]): number {
  if (!sorted.length) return 0
  const mid = Math.floor(sorted.length / 2)
  return sorted.length % 2 === 0 ? (sorted[mid - 1] + sorted[mid]) / 2 : sorted[mid]
}

/**
 * Ordinary Least Squares linear regression.
 * Returns { slope, intercept } where y = slope * x + intercept.
 */
function linearRegression(xs: number[], ys: number[]): { slope: number; intercept: number } {
  const n = xs.length
  if (n < 2) return { slope: 0, intercept: ys[0] ?? 0 }
  const xMean = mean(xs)
  const yMean = mean(ys)
  let num = 0
  let den = 0
  for (let i = 0; i < n; i++) {
    num += (xs[i] - xMean) * (ys[i] - yMean)
    den += (xs[i] - xMean) ** 2
  }
  const slope = den === 0 ? 0 : num / den
  return { slope, intercept: yMean - slope * xMean }
}

/**
 * Simple exponential smoothing (α controls smoothing level).
 * Returns smoothed series the same length as input.
 */
function exponentialSmoothing(values: number[], alpha = 0.3): number[] {
  if (!values.length) return []
  const smoothed: number[] = [values[0]]
  for (let i = 1; i < values.length; i++) {
    smoothed.push(alpha * values[i] + (1 - alpha) * smoothed[i - 1])
  }
  return smoothed
}

/**
 * Detect weekly seasonality by computing average values for each day-of-week
 * across all data points. Returns normalised day weights (length 7).
 */
function detectSeasonality(points: CapacityDataPoint[]): { detected: boolean; weights: number[] } {
  const buckets: number[][] = Array.from({ length: 7 }, () => [])
  for (const p of points) {
    const dow = new Date(p.timestamp).getDay()
    buckets[dow].push(p.operationCount)
  }
  const dayMeans = buckets.map((b) => (b.length ? mean(b) : 0))
  const overall = mean(dayMeans.filter((v) => v > 0))
  if (overall === 0) return { detected: false, weights: new Array(7).fill(1) }
  const weights = dayMeans.map((m) => (m > 0 ? m / overall : 1))
  // If max deviation from 1.0 is significant, seasonality is present
  const maxDev = Math.max(...weights.map((w) => Math.abs(w - 1)))
  return { detected: maxDev > 0.15, weights }
}

/**
 * Count statistical anomalies: points whose ops value is more than 2.5 std devs
 * from the mean.
 */
function countAnomalies(values: number[]): number {
  if (values.length < 4) return 0
  const m = mean(values)
  const sd = stdDev(values, m)
  if (sd === 0) return 0
  return values.filter((v) => Math.abs(v - m) > 2.5 * sd).length
}

