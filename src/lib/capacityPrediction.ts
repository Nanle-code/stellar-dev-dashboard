// ---------------------------------------------------------------------------

/**
 * Runs the full capacity prediction pipeline on the provided data points.
 * This is the primary entry point for the load balancing system's prediction
 * component. It feeds into the dynamic load distribution algorithm to:
 *   1. Adjust rate limiter thresholds based on predicted congestion
 *   2. Prioritize cache warming for predicted high-load periods
 *   3. Pre-scale circuit breaker sensitivity for predicted failure windows
 *   4. Generate infrastructure recommendations for capacity planning
 *
 * Pipeline steps:
 *   1. Validate data sufficiency (≥5 points required)
 *   2. Smooth and detrend historical operations
 *   3. Detect weekly seasonality patterns
 *   4. Count statistical anomalies (>2.5σ from mean)
 *   5. Estimate model accuracy via walk-forward validation
 *   6. Generate time-series predictions for the horizon
 *   7. Build three growth scenarios (conservative/moderate/aggressive)
 *   8. Analyze feature adoption trends from operation-type distribution
 *   9. Generate infrastructure recommendations with priority levels
 *   10. Produce natural-language insights for dashboards
 *
 * @param points   Array of CapacityDataPoint (from ledger history / monitoring)
 * @param horizonDays  How many days ahead to forecast (default: 30)
 * @param capacityLimit  Maximum ops the network supports (default: CAPACITY_LIMIT_OPS)
 */
export function predictCapacity(
  points: CapacityDataPoint[],
  horizonDays: number = 30,
  capacityLimit: number = CAPACITY_LIMIT_OPS,
): CapacityPredictionResult {
  const analyzedAt = new Date().toISOString()

  if (points.length < MIN_DATA_POINTS) {
    const empty: CapacityPlanningReport = {
      generatedAt: analyzedAt,
      forecastWindowDays: horizonDays,
      currentUtilisation: 0,
      utilizationTrend: 'stable',
      predictions: [],
      scenarios: buildScenarios(0, horizonDays, capacityLimit),
      featureAdoption: [],
      recommendations: [
        {
          id: 'need-data',
          priority: 'medium',
          category: 'monitoring',
          title: 'Insufficient Data for Forecasting',
          description: `At least ${MIN_DATA_POINTS} data points are required. Currently have ${points.length}.`,
          timeframe: 'Ongoing',
          estimatedImpact: 'Enables accurate capacity forecasting',
          actionItems: [
            'Ensure the ledger streaming connection is active',
            'Wait for at least 5 ledger entries to accumulate',
            'Check that networkStats are being collected correctly',
          ],
        },
      ],
      insights: ['Insufficient historical data — predictions will appear once at least 5 data points have been collected.'],
      modelAccuracy: 0,
      dataQuality: 'poor',
      summary: 'Insufficient data for capacity prediction. Accumulate more ledger history to enable forecasting.',
    }

    return {
      report: empty,
      trendSlope: 0,
      seasonalityDetected: false,
      anomaliesDetected: 0,
      baselineOps: 0,
      capacityLimitOps: capacityLimit,
      analyzedAt,
      dataPoints: points.length,
    }
  }

  const sorted = [...points].sort(
    (a, b) => new Date(a.timestamp).getTime() - new Date(b.timestamp).getTime(),
  )

  const ops = sorted.map((p) => p.operationCount)
  const smoothed = exponentialSmoothing(ops, 0.25)
  const xs = ops.map((_, i) => i)
  const { slope } = linearRegression(xs, smoothed)

  const baselineOps = Math.round(mean(ops.slice(-10))) // rolling last-10 average
  const currentUtilisation = Math.min(1, baselineOps / capacityLimit)
  const { detected: seasonalityDetected } = detectSeasonality(sorted)
  const anomaliesDetected = countAnomalies(ops)
  const modelAccuracy = estimateModelAccuracy(sorted)

  const predictions = forecastTimeSeries(sorted, horizonDays)
  const scenarios = buildScenarios(baselineOps, horizonDays, capacityLimit)
  const featureAdoption = analyseFeatureAdoption(sorted)

  const lastPoint = sorted[sorted.length - 1]
  const failedTxPercent = lastPoint?.failedTxPercent ?? 0

  const recommendations = generateRecommendations(
    currentUtilisation,
    slope,
    scenarios,
    anomaliesDetected,
    failedTxPercent,
  )

  const utilizationTrend = classifyUtilisationTrend(slope, baselineOps)
  const insights = generateInsights(
    currentUtilisation,
    slope,
    predictions,
    scenarios,
    seasonalityDetected,
    anomaliesDetected,
    modelAccuracy,
  )

  const criticalCount = recommendations.filter((r) => r.priority === 'critical').length
  const highCount = recommendations.filter((r) => r.priority === 'high').length

  const summary =
    criticalCount > 0
      ? `${criticalCount} critical action${criticalCount !== 1 ? 's' : ''} required. Immediate scaling intervention needed.`
      : highCount > 0
        ? `${highCount} high-priority recommendation${highCount !== 1 ? 's' : ''} identified. Review scaling plan within the next week.`
        : `Capacity looks healthy at ${Math.round(currentUtilisation * 100)}% utilisation. Continue monitoring growth trends.`

  const report: CapacityPlanningReport = {
    generatedAt: analyzedAt,
    forecastWindowDays: horizonDays,
    currentUtilisation,
    utilizationTrend,
    predictions,
    scenarios,
    featureAdoption,
    recommendations,
    insights,
    modelAccuracy,
    dataQuality: assessDataQuality(sorted),
    summary,
  }

  return {
    report,
    trendSlope: parseFloat(slope.toFixed(3)),
    seasonalityDetected,
    anomaliesDetected,
    baselineOps,
    capacityLimitOps: capacityLimit,
    analyzedAt,
    dataPoints: points.length,
  }
}

// ---------------------------------------------------------------------------
// Adapter: convert LedgerStatsEntry[] (from Zustand store) to CapacityDataPoint[]
// ---------------------------------------------------------------------------

import type { LedgerStatsEntry } from './store'

/**
 * Converts the store's ledgerHistory entries into CapacityDataPoint[]
 * ready for the prediction engine.
 */
export function ledgerHistoryToCapacityPoints(
  history: LedgerStatsEntry[],
): CapacityDataPoint[] {
  return history.map((entry) => {
    const total = entry.txSuccessCount + entry.txFailedCount
    return {
      timestamp: entry.closedAt,
      operationCount: entry.operationCount,
      txCount: total,
      baseFee: entry.baseFee,
      congestionRatio: Math.min(1, entry.operationCount / CAPACITY_LIMIT_OPS),
      failedTxPercent: total > 0 ? (entry.txFailedCount / total) * 100 : 0,
    }
  })
}
