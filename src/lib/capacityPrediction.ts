/** A single observed data point for a capacity metric */
export interface CapacityDataPoint {
  /** ISO-8601 timestamp */
  timestamp: string
  /** Operation count for this ledger/period */
  operationCount: number
  /** Transaction count for this period */
  txCount: number
  /** Base fee in stroops */
  baseFee: number
  /** Congestion ratio 0-1 */
  congestionRatio: number
  /** Failed tx percentage 0-100 */
  failedTxPercent: number
}

/** A single predicted data point returned by the forecasting model */
export interface PredictionPoint {
  /** ISO-8601 timestamp of the predicted period */
  timestamp: string
  /** Predicted operation count */
  predictedOps: number
  /** Lower bound of 80% confidence interval */
  lowerBound: number
  /** Upper bound of 80% confidence interval */
  upperBound: number
  /** Model confidence 0-1 */
  confidence: number
}

