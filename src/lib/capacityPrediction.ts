/**
 * Infers feature adoption metrics from operation-type distribution in the
 * capacity data. Uses tx count trends as a proxy for feature growth.
 */
export function analyseFeatureAdoption(
  points: CapacityDataPoint[],
): FeatureAdoptionMetric[] {
  if (points.length < 2) return []

  const sorted = [...points].sort(
    (a, b) => new Date(a.timestamp).getTime() - new Date(b.timestamp).getTime(),
  )

  // Derive proxy metrics from the raw time-series
  const txCounts = sorted.map((p) => p.txCount)
  const fees = sorted.map((p) => p.baseFee)
  const congestions = sorted.map((p) => p.congestionRatio)

  const txGrowth = txCounts.length >= 2
    ? (txCounts[txCounts.length - 1] - txCounts[0]) / Math.max(txCounts[0], 1)
    : 0
  const feeGrowth = fees.length >= 2
    ? (fees[fees.length - 1] - fees[0]) / Math.max(fees[0], 1)
    : 0
  const congGrowth = congestions.length >= 2
    ? congestions[congestions.length - 1] - congestions[0]
    : 0

  const clamp = (v: number, lo = 0, hi = 1) => Math.min(hi, Math.max(lo, v))

  const metrics: FeatureAdoptionMetric[] = [
    {
      feature: 'Payment Operations',
      usageRate: clamp(mean(congestions)),
      growthRate: clamp(txGrowth / 10),
      projectedUsageIn30Days: clamp(mean(congestions) + txGrowth * 0.3),
    },
    {
      feature: 'Smart Contracts (Soroban)',
      usageRate: clamp(mean(congestions) * 0.35),
      growthRate: clamp(txGrowth / 8),
      projectedUsageIn30Days: clamp(mean(congestions) * 0.35 + txGrowth * 0.35 * 0.3),
    },
    {
      feature: 'DEX / Liquidity Pools',
      usageRate: clamp(mean(congestions) * 0.25),
      growthRate: clamp(Math.abs(feeGrowth) / 5),
      projectedUsageIn30Days: clamp(mean(congestions) * 0.25 + Math.abs(feeGrowth) * 0.3),
    },
    {
      feature: 'Claimable Balances',
      usageRate: clamp(mean(congestions) * 0.10),
      growthRate: clamp(congGrowth),
      projectedUsageIn30Days: clamp(mean(congestions) * 0.10 + congGrowth * 0.3),
    },
  ]

  return metrics.map((m) => ({
    ...m,
    usageRate: parseFloat(m.usageRate.toFixed(3)),
    growthRate: parseFloat(m.growthRate.toFixed(3)),
    projectedUsageIn30Days: parseFloat(m.projectedUsageIn30Days.toFixed(3)),
  }))
}

