// ---------------------------------------------------------------------------
// Natural-language insights
// ---------------------------------------------------------------------------

export function generateInsights(
  currentUtilisation: number,
  trendSlope: number,
  predictions: PredictionPoint[],
  scenarios: ScenarioProjection[],
  seasonalityDetected: boolean,
  anomaliesDetected: number,
  modelAccuracy: number,
): string[] {
  const insights: string[] = []

  insights.push(
    `Current network utilisation is ${Math.round(currentUtilisation * 100)}% of the ledger operation limit (${CAPACITY_LIMIT_OPS} ops/ledger).`,
  )

  if (trendSlope > 0) {
    insights.push(
      `Usage is growing at approximately ${trendSlope.toFixed(1)} operations/day — indicating healthy network adoption.`,
    )
  } else if (trendSlope < 0) {
    insights.push(
      `Usage is declining at ${Math.abs(trendSlope).toFixed(1)} operations/day — consider investigating integration health.`,
    )
  } else {
    insights.push('Usage is stable with no significant growth or decline trend detected.')
  }

  if (predictions.length > 0) {
    const last = predictions[predictions.length - 1]
    insights.push(
      `The model predicts ~${last.predictedOps.toLocaleString()} operations at the end of the ${predictions.length}-day forecast window (confidence: ${Math.round(last.confidence * 100)}%).`,
    )
  }

  const aggressiveScenario = scenarios.find((s) => s.scenario === 'aggressive')
  if (aggressiveScenario?.daysToCapacityLimit !== null && aggressiveScenario?.daysToCapacityLimit !== undefined) {
    insights.push(
      `Under aggressive growth (75%/yr), the capacity limit could be reached in ${aggressiveScenario.daysToCapacityLimit} days.`,
    )
  }

  const conservativeScenario = scenarios.find((s) => s.scenario === 'conservative')
  if (conservativeScenario?.daysToCapacityLimit === null) {
    insights.push('Under conservative growth, the capacity limit is not expected to be reached within the forecast window.')
  }

  if (seasonalityDetected) {
    insights.push('Weekly seasonality detected — usage varies significantly by day of week, which has been factored into the forecast.')
  }

  if (anomaliesDetected > 0) {
    insights.push(
      `${anomaliesDetected} anomalous data point${anomaliesDetected !== 1 ? 's' : ''} detected. These may represent traffic spikes or data gaps.`,
    )
  }

  insights.push(
    `Forecast model estimated accuracy: ${Math.round(modelAccuracy * 100)}%. Accuracy improves with more historical data points.`,
  )

  return insights
}

// ---------------------------------------------------------------------------
// Utilisation trend classification
// ---------------------------------------------------------------------------

function classifyUtilisationTrend(
  trendSlope: number,
  baselineOps: number,
): 'increasing' | 'stable' | 'decreasing' {
  if (baselineOps === 0) return 'stable'
  const relativeSlope = trendSlope / baselineOps
  if (relativeSlope > 0.02) return 'increasing'
  if (relativeSlope < -0.02) return 'decreasing'
  return 'stable'
}

// ---------------------------------------------------------------------------
// Data quality assessment
// ---------------------------------------------------------------------------

function assessDataQuality(points: CapacityDataPoint[]): 'good' | 'fair' | 'poor' {
  if (points.length >= 20) return 'good'
  if (points.length >= MIN_DATA_POINTS) return 'fair'
  return 'poor'
}

// ---------------------------------------------------------------------------
// Model accuracy estimation
// ---------------------------------------------------------------------------

/**
 * Estimates model accuracy using leave-one-out cross-validation on the last
 * 20% of data points (walk-forward validation).
 * Returns a value 0-1 representing mean directional accuracy.
 */
function estimateModelAccuracy(points: CapacityDataPoint[]): number {
  const n = points.length
  if (n < MIN_DATA_POINTS * 2) return 0.65 // not enough data, return conservative estimate

  const validationSize = Math.max(2, Math.floor(n * 0.2))
  const trainEnd = n - validationSize

  const trainPoints = points.slice(0, trainEnd)
  const valPoints = points.slice(trainEnd)

  const trainOps = trainPoints.map((p) => p.operationCount)
  const trainXs = trainOps.map((_, i) => i)
  const { slope, intercept } = linearRegression(trainXs, exponentialSmoothing(trainOps, 0.25))

  let correctDirections = 0
  for (let i = 0; i < valPoints.length; i++) {
    const predicted = slope * (trainEnd + i) + intercept
    const actual = valPoints[i].operationCount
    const prevActual = i === 0
      ? trainPoints[trainEnd - 1]?.operationCount ?? actual
      : valPoints[i - 1].operationCount

    const predictedDir = predicted >= (slope * (trainEnd + i - 1) + intercept) ? 1 : -1
    const actualDir = actual >= prevActual ? 1 : -1
    if (predictedDir === actualDir) correctDirections++
  }

  return parseFloat((correctDirections / valPoints.length).toFixed(2))
}

