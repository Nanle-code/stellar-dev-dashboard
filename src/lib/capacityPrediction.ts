const SCENARIO_CONFIGS: Record<
  ScenarioType,
  { annualGrowthRate: number; label: string; color: string }
> = {
  conservative: { annualGrowthRate: 0.10, label: 'Conservative (10%/yr)', color: 'var(--cyan)' },
  moderate:     { annualGrowthRate: 0.30, label: 'Moderate (30%/yr)',     color: 'var(--amber)' },
  aggressive:   { annualGrowthRate: 0.75, label: 'Aggressive (75%/yr)',   color: 'var(--red)' },
}

/**
 * Computes scenario projections for all three growth rates.
 * Each scenario applies a daily compound growth on top of the baseline OPS.
 */
export function buildScenarios(
  baselineOps: number,
  horizonDays: number,
  capacityLimit: number = CAPACITY_LIMIT_OPS,
): ScenarioProjection[] {
  const MS_PER_DAY = 86_400_000
  const now = Date.now()

  return (Object.entries(SCENARIO_CONFIGS) as [ScenarioType, typeof SCENARIO_CONFIGS[ScenarioType]][]).map(
    ([scenario, cfg]) => {
      const dailyRate = (1 + cfg.annualGrowthRate) ** (1 / 365) - 1
      const points: PredictionPoint[] = []
      let daysToLimit: number | null = null

      for (let d = 1; d <= horizonDays; d++) {
        const predicted = Math.round(baselineOps * (1 + dailyRate) ** d)
        const spread = predicted * 0.12 // ±12% envelope
        const confidence = Math.max(0.45, 1 - (d / horizonDays) * 0.35)
        points.push({
          timestamp: new Date(now + d * MS_PER_DAY).toISOString(),
          predictedOps: predicted,
          lowerBound: Math.max(0, Math.round(predicted - spread)),
          upperBound: Math.round(predicted + spread),
          confidence: parseFloat(confidence.toFixed(2)),
        })
        if (daysToLimit === null && predicted >= capacityLimit) {
          daysToLimit = d
        }
      }

      const projectedPeakOps = points[points.length - 1]?.predictedOps ?? baselineOps

      return {
        scenario,
        annualGrowthRate: cfg.annualGrowthRate,
        label: cfg.label,
        color: cfg.color,
        points,
        daysToCapacityLimit: daysToLimit,
        projectedPeakOps,
      } satisfies ScenarioProjection
    },
  )
}

