interface FileMeta {
  coupling: number;
  isPublicApi: boolean;
  /** Raw source text (used by detectors that need lexical inspection). */
  source: string;
}

/**
 * Cheap heuristic coupling counter: count `import` statements and the
 * number of distinct specifiers. Used to inform safety scoring.
 */
function buildFileMetadata(files: FileTarget[]): Map<string, FileMeta> {
  const out = new Map<string, FileMeta>();
  for (const f of files) {
    const imports = f.source.match(/^\s*import\b[^\n]*$/gm) ?? [];
    let coupling = 0;
    for (const line of imports) {
      const named = (line.match(/\{([^}]+)\}/g) ?? [])
        .join(',')
        .split(',')
        .filter(Boolean).length;
      coupling += named + 1;
    }
    const isPublicApi = /export\s+(default|function|class|const|interface|type)/m.test(
      f.source
    );
    out.set(f.path, { coupling, isPublicApi, source: f.source });
  }
  return out;
}

/**
 * Detect hotspot files via an unsupervised Isolation Forest trained on the
 * corpus. Files whose vector deviates furthest from the corpus are surfaced
 * for deeper manual review.
 */
function detectHotspots(
  metrics: ReturnType<typeof analyzeFiles>,
  baseline: CorpusBaseline
): Hotspot[] {
  if (metrics.length < 2) return [];

  // Feature vector: [loc, averageComplexity, anyUsage, duplicateLiteralScore]
  const X = metrics.map((f) => [
    f.loc,
    f.averageFunctionComplexity,
    f.anyUsage,
    f.duplicateLiteralScore,
  ]);

  const forest = new IsolationForest();
  forest.fit(X);

  return metrics
    .map((file, idx) => {
      const anomaly = forest.score(X[idx]);
      const factors: string[] = [];
      if (file.loc > baseline.averageLoc * 1.5) factors.push(`LOC ${file.loc} vs avg ${Math.round(baseline.averageLoc)}`);
      if (file.averageFunctionComplexity > baseline.averageComplexity * 1.5)
        factors.push(`avg complexity ${file.averageFunctionComplexity.toFixed(1)}`);
      if (file.anyUsage > baseline.averageAnyUsage * 1.5)
        factors.push(`${file.anyUsage} \`any\` annotations`);
      if (file.duplicateLiteralScore > 1) factors.push(`${file.duplicateLiteralScore} duplicate literals`);
      if (factors.length === 0) factors.push('Multivariate outlier');
      return {
        file: file.path,
        anomalyScore: Number(anomaly.toFixed(2)),
        contributingFactors: factors,
        baseline: {
          avgLoc: Math.round(baseline.averageLoc),
          avgComplexity: Number(baseline.averageComplexity.toFixed(1)),
          avgParams: Number(baseline.averageParameters.toFixed(1)),
        },
      };
    })
    .sort((a, b) => b.anomalyScore - a.anomalyScore)
    .slice(0, 10);
}
