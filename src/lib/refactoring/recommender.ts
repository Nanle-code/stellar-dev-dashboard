// ---------------------------------------------------------------------------
// Pseudo Isolation Forest for hotspot discovery
// ---------------------------------------------------------------------------

/**
 * Lightweight Isolation Forest implementation. Each tree splits on a
 * random feature at a random value within its observed range.  Anomaly
 * score is `2^(-mean_path_length / c(n))`. This is the same family as the
 * one used in the existing transaction-patterns module — kept
 * self-contained so the recommender does not pull in TFJS at runtime.
 */
interface IFNode {
  feature: number;
  value: number;
  size: number;
  left: IFNode | null;
  right: IFNode | null;
}

class IsolationForest {
  private trees: IFNode[] = [];
  constructor(
    private nTrees: number = 60,
    private subSampleSize: number = 64
  ) {}

  fit(X: number[][]): void {
    if (X.length === 0) return;
    const n = X.length;
    const limit = Math.ceil(Math.log2(Math.max(this.subSampleSize, 2)));
    this.trees = [];
    for (let t = 0; t < this.nTrees; t++) {
      const idx: number[] = [];
      while (idx.length < Math.min(this.subSampleSize, n)) {
        const r = Math.floor(Math.random() * n);
        if (!idx.includes(r)) idx.push(r);
      }
      const sample = idx.map((i) => X[i]);
      this.trees.push(this.buildTree(sample, 0, limit, X[0].length));
    }
  }

  private buildTree(
    X: number[][],
    curDepth: number,
    limit: number,
    featureCount: number
  ): IFNode {
    if (X.length <= 1 || curDepth >= limit) {
      return { feature: -1, value: 0, size: X.length, left: null, right: null };
    }
    const featureIdx = Math.floor(Math.random() * featureCount);
    let min = Infinity;
    let max = -Infinity;
    for (const row of X) {
      const v = row[featureIdx];
      if (v < min) min = v;
      if (v > max) max = v;
    }
    if (!Number.isFinite(min) || !Number.isFinite(max) || min === max) {
      return { feature: -1, value: 0, size: X.length, left: null, right: null };
    }
    const value = min + Math.random() * (max - min);
    const left = X.filter((r) => r[featureIdx] < value);
    const right = X.filter((r) => r[featureIdx] >= value);
    return {
      feature: featureIdx,
      value,
      size: X.length,
      left: this.buildTree(left, curDepth + 1, limit, featureCount),
      right: this.buildTree(right, curDepth + 1, limit, featureCount),
    };
  }

  score(x: number[]): number {
    if (!this.trees.length) return 0;
    const lengths = this.trees.map((t) => pathLen(x, t, 0));
    const avg = lengths.reduce((s, l) => s + l, 0) / lengths.length;
    const n = Math.min(this.subSampleSize, 256);
    const cn = cFactor(n);
    if (cn === 0) return 0;
    return Math.pow(2, -avg / cn);
  }
}

function pathLen(x: number[], node: IFNode, depth: number): number {
  if (!node.left || !node.right) {
    return depth + cFactor(Math.max(1, node.size));
  }
  const v = node.feature >= 0 ? x[node.feature] ?? 0 : 0;
  if (v < node.value && node.left) return pathLen(x, node.left, depth + 1);
  if (node.right) return pathLen(x, node.right, depth + 1);
  return depth + 1;
}

function cFactor(n: number): number {
  if (n <= 1) return 0;
  if (n === 2) return 1;
  return 2 * (Math.log(n - 1) + 0.5772156649) - (2 * (n - 1)) / n;
}

