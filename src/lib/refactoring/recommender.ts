export interface RecommendInput {
  files: FileTarget[];
  options?: RecommendOptions;
  /** Files that are auto-generated and unsafe to edit (defaults to none) */
  autoGeneratedFiles?: Set<string>;
  /** Files exposed as public API (defaults to none) */
  publicApiFiles?: Set<string>;
}

export interface RecommendResult {
  report: RefactorReport;
  /** Per-file metrics for the dashboard UI */
  metrics: ReturnType<typeof analyzeFiles>;
  /** Suggestions after prioritization */
  suggestions: ReturnType<typeof rankSuggestions>;
  /** Suggestions sorted into quick wins */
  quickWins: ReturnType<typeof rankSuggestions>;
}

export function recommend(input: RecommendInput): RecommendResult {
  const filter = input.options?.fileFilter;
  const files = filter ? input.files.filter((f) => filter(f.path)) : input.files;
  const metrics = analyzeFiles(files);
  const metadata = buildFileMetadata(files);

  const safetyCtx: SafetyContext = buildSafetyContext(
    files,
    input.autoGeneratedFiles ?? new Set(),
    input.publicApiFiles ?? new Set()
  );

  const baseline = buildBaseline(metrics);
  const hotspots = detectHotspots(metrics, baseline);

  // Run detectors per file and combine suggestions.
  const allSuggestions: RefactoringSuggestion[] = [];
  for (const file of metrics) {
    const meta = metadata.get(file.path) ?? {
      coupling: 0,
      isPublicApi: false,
    };
    const source = meta.source ?? '';
    const raw = runDetectors({
      file,
      source,
      coveredByTests: safetyCtx.testCoverage.has(file.path),
      isPublicApi: meta.isPublicApi || input.publicApiFiles?.has(file.path) === true,
      coupling: meta.coupling,
    });
    for (const s of raw) {
      const impact = computeImpact(s, file, baseline);
      const safety = assessSafety(s, safetyCtx).safety;
      const priority = computePriority({ ...s, safety }, impact);
      allSuggestions.push({ ...s, safety, priority });
    }
  }

  const ranked = rankSuggestions(
    allSuggestions,
    Object.fromEntries(allSuggestions.map((s) => [s.id, s.safety]))
  );

  // Apply trims/locks
  const minPriority = input.options?.minPriority ?? 0;
  const max = input.options?.maxSuggestions ?? 200;
  const filtered = ranked.filter((s) => {
    if (s.priority < minPriority) return false;
    if (!input.options?.includeRisky && s.bucket === 'high-effort' && s.safety < 0.3)
      return false;
    return true;
  }).slice(0, max);

  const breakdown: Record<RefactorKind, number> = {
    'extract-function': 0,
    'simplify-conditional': 0,
    'reduce-complexity': 0,
    'remove-dead-code': 0,
    'rename-symbol': 0,
    'replace-any': 0,
    'split-file': 0,
    memoize: 0,
    'inline-trivial': 0,
    'consolidate-duplicates': 0,
    'reduce-parameters': 0,
    'modernize-syntax': 0,
    'add-type-annotation': 0,
    'flatten-nesting': 0,
  };
  for (const s of filtered) breakdown[s.kind] = (breakdown[s.kind] ?? 0) + 1;

  const totalLinesSaved = filtered.reduce((sum, s) => sum + s.linesSaved, 0);
  const totalComplexity = filtered.reduce((sum, s) => sum + s.complexityDelta, 0);
  const totalMaintainability = filtered.reduce(
    (sum, s) => sum + s.maintainabilityDelta,
    0
  );
  const avgAcceptance = filtered.length
    ? filtered.reduce((sum, s) => sum + s.confidence * s.safety, 0) / filtered.length
    : 0;
  const overallQuality = metrics.length
    ? metrics.reduce((sum, f) => sum + maintainabilityIndex(f), 0) /
      metrics.length
    : 0;

  const report: RefactorReport = {
    generatedAt: new Date().toISOString(),
    totalFilesAnalysed: metrics.length,
    totalFunctionsAnalysed: metrics.reduce((s, f) => s + f.functions.length, 0),
    totalSuggestions: filtered.length,
    averageAcceptanceProbability: Number((avgAcceptance * 100).toFixed(1)),
    codeQualityScore: Number(Math.max(0, Math.min(100, overallQuality)).toFixed(1)),
    hotspots,
    suggestions: filtered,
    issueBreakdown: breakdown,
    impact: {
      totalLinesSaved,
      totalComplexityReduction: totalComplexity,
      totalMaintainabilityGain: totalMaintainability,
    },
  };

  const quickWins = filtered.filter(
    (s) => s.priority >= 70 && s.safety >= 0.7 && s.effortMinutes <= 25
  );

  return { report, metrics, suggestions: filtered, quickWins };
}

